// Bot i18n — user-facing strings only. Admin panel + payment reference
// verifier messages stay in English for the operator.

export type Lang = 'en' | 'hi' | 'es' | 'ru' | 'vi' | 'fr' | 'pt' | 'ar';

export const SUPPORTED_LANGS: Lang[] = ['en', 'hi', 'es', 'ru', 'vi', 'fr', 'pt', 'ar'];

export const LANG_META: Record<Lang, { flag: string; native: string }> = {
  en: { flag: '🇬🇧', native: 'English' },
  hi: { flag: '🇮🇳', native: 'हिन्दी' },
  es: { flag: '🇪🇸', native: 'Español' },
  ru: { flag: '🇷🇺', native: 'Русский' },
  vi: { flag: '🇻🇳', native: 'Tiếng Việt' },
  fr: { flag: '🇫🇷', native: 'Français' },
  pt: { flag: '🇵🇹', native: 'Português' },
  ar: { flag: '🇸🇦', native: 'العربية' },
};

export function normalizeLang(v: unknown): Lang {
  const s = String(v ?? '').toLowerCase().slice(0, 2);
  return (SUPPORTED_LANGS as string[]).includes(s) ? (s as Lang) : 'en';
}

